// Question-7:   Implement a switch case statement to determine the type of fruit based on a variable. Handle at least three different fruit options.

let fruit = "Mango";

switch (fruit) {
    case "Mango":
        console.log("King of fruit");
        break;
    case "Orange":
        console.log("Round orange");
        break;
    case "Apple":
        console.log("Red");
        break;

    default:
        break;
}